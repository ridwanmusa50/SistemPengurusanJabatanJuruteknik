package com.sistempengurusanjabatanjuruteknik.ui.pengadu.membuatAduan;


public class Aduan {
    public String idAduan, tarikhAduan, masaAduan, namaPenuhPengadu, idMesin, huraianAduan;

    public Aduan(String idAduan, String tarikhAduan, String masaAduan, String namaPenuhPengadu, String idMesin, String huraianAduan)
    {
        this.idAduan = idAduan;
        this.tarikhAduan = tarikhAduan;
        this.masaAduan = masaAduan;
        this.namaPenuhPengadu = namaPenuhPengadu;
        this.idMesin = idMesin;
        this.huraianAduan = huraianAduan;
    }
}
